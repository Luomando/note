一、安装
1、首先要安装好composer
2、安装laravel包，运行：composer global require "laravel/installer=~1.1"
3、添加laravel的环境变量，在PATH中添加：C:\Users\Administrator\AppData\Roaming\Composer\vendor\bin（根据电脑用户不同可能不是Admin用户）
4、创建laravel应用，使用“laravel new 应用名”创建，如要创建一个名为blog的laravel应用：进入服务器目录（如www目录），运行”laravel new blog“就会在www目录中创建一个blog目录，里面有laravel的全新文件